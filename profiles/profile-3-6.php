 <!-- ==================== WHAT IS THE 3/6 PROFILE? ==================== -->
 <section class="py-10 md:py-14 bg-gm-light">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> What Is the 3/6 Profile?
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 3/6 Profile combines the <strong class="text-gray-800">Martyr</strong>, Line 3, with the <strong class="text-gray-800">Role Model</strong>, Line 6, creating one of the most developmental profiles in Human Design. The 3/6 moves through three broad life phases, each bringing a different relationship to experience, learning, and wisdom.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4"><strong class="text-gray-800">Phase 1, birth to around 30:</strong> The 3/6 often lives like a double 3rd line, learning through trial and error, experimentation, and bonds made and broken. This phase can feel intense, unstable, and at times chaotic. Both lines are operating through the 3rd line process, making this the most experimental period of the 3/6's life.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4"><strong class="text-gray-800">Phase 2, around 30 to 50:</strong> The 6th line goes on the roof. The 3/6 begins to step back from constant immersion and turns more toward observation, reflection, and integration. They often become more selective and more discerning. The 3rd line process is still present, but the 6th line begins to add distance, perspective, and a broader view.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4"><strong class="text-gray-800">Phase 3, around 50 and beyond:</strong> The 6th line comes off the roof. The 3/6 steps more fully into role model energy, not through theory, but through deeply lived experience. What has been learned through time, trial, and reflection can now be embodied and shared through presence, example, and wisdom.</p>
 </div>
 </section>
 <!-- ==================== HOW THE 3 AND 6 WORK TOGETHER ==================== -->
 <section class="py-10 md:py-14 bg-white">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6">
How the 3 and 6 Work Together
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 3/6 Profile combines experimentation with long-term maturation. Line 3 learns through direct involvement in life. It tests, adapts, and discovers through experience, often by seeing what does not work before understanding what does. Line 6 adds a larger life arc. It develops in stages, gradually moving from immersion and trial into perspective, wisdom, and role model presence.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">This means the 3/6 often has a particularly developmental path. Early life may feel full of testing, disruption, change, and discovery through lived experience. Over time, however, those experiences are not wasted. The 6 brings distance, integration, and the capacity to make meaning out of what has been lived.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">When these two lines work well together, the result is <strong class="text-gray-800">wisdom that has been earned</strong>. The 3 brings realism, resilience, and flexibility. The 6 brings perspective, maturity, and a broader view of life's patterns. Together, they create someone whose authority grows through experience and whose example becomes stronger with time.</p>
 </div>
 </section>
 <!-- ==================== LIVING THE 3/6 ==================== -->
 <section class="py-10 md:py-14 bg-gm-light">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> Living the 3/6
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 3/6 journey requires patience and trust in the process. Early life can feel intense, unpredictable, and full of trial and error, but these experiences are not meaningless. Over time, they become part of the foundation for the wisdom, perspective, and discernment that the 6th line brings.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">This is why Strategy and Authority matter so much for the 3/6. They help you move through each phase of life in a way that is correct for you, rather than getting lost in unnecessary struggle or pressure. When a 3/6 trusts their process, they gradually turn lived experience into grounded wisdom. This is what makes them an authentic role model, not perfection, but experience that has been tested, reflected on, and integrated.</p>

 <div class="bg-gm-card-active border-l-4 border-gm-purple rounded-r-lg p-5 my-6">
 <p class="text-xs font-semibold text-gm-purple uppercase tracking-widest mb-2">Key Insight</p>
 <p class="text-gm-text text-sm leading-relaxed italic">Remember: your Profile operates within your Type and Authority. A 3/6 Generator lives this profile very differently from a 3/6 Projector. The Profile describes your role, while your Type and Authority describe how you enter into that role correctly.</p>
 </div>
 </div>
 </section>
 <!-- ==================== THE 3/6 IN RELATIONSHIPS ==================== -->
 <section class="py-10 md:py-14 bg-white">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> The 3/6 in Relationships
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">In relationships, the 3/6 often changes over time more noticeably than many other profiles. In the earlier phase of life, relationships may involve more trial and error as the 3rd line learns through direct experience. This can make early bonds feel intense, changeable, or difficult to sustain, not because the 3/6 is insincere, but because they are discovering what is real through lived reality.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">As the 6th line matures, the 3/6 often becomes more observant, selective, and reflective in relationship. There is usually less impulse to stay immersed in every experience and more desire to step back, see clearly, and choose what is truly aligned. Over time, this can bring a more mature and discerning approach to connection.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">In later life, relationships often carry the depth of lived experience and hard-earned wisdom. A healthy 3/6 can bring <strong class="text-gray-800">honesty, perspective, resilience, and authenticity</strong> into relationship. They do best with people who respect their process, understand that they mature through experience, and value the depth that comes with a life that has truly been lived.</p>
 </div>
 </section>
