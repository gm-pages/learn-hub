 <section class="py-10 md:py-14 bg-gm-light">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> What Is the 5/2 Profile?
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 5/2 Profile combines the <strong class="text-gray-800">Heretic</strong>, Line 5, with the <strong class="text-gray-800">Hermit</strong>, Line 2. This creates a compelling tension: the 5th line attracts projection and attention from the outside world, while the 2nd line needs solitude and often prefers to be left alone. The 5/2 may be called out to offer solutions and then need to retreat in order to recharge and allow its natural gifts to develop.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The conscious 5th line carries the <strong class="text-gray-800">projection field</strong>. Others often see practical solutions in the 5/2 and expect delivery. This projecting quality can be powerful and difficult to avoid.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The unconscious 2nd line carries <strong class="text-gray-800">natural gifts</strong> that may not always be fully recognized by the person themselves. The Hermit needs withdrawal, time alone to integrate, develop, and simply be. The 5/2 tension lies between being called out by outer expectation and needing retreat in order to remain connected to what is natural.</p>
