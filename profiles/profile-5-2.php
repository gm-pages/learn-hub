How the 5 and 2 Work Together
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 5/2 Profile combines public expectation with private naturalness. Line 5 tends to attract projection, responsibility, and the sense that others want something from it. Line 2, by contrast, is naturally inward. It needs privacy, ease, and the space to let its gifts emerge without too much interference or pressure.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">This can create a push-pull dynamic in the 5/2. Part of you may want a quiet life, while another part finds that others keep drawing you into visibility or assigning roles to you. At times, this can feel uncomfortable or even distorting, especially when you are being seen through projection rather than for who you actually are.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">When these two lines work well together, the result is <strong class="text-gray-800">quietly powerful impact</strong>. The 2 brings authenticity, instinct, and effortless ability. The 5 brings influence, practicality, and broad usefulness. Together, they create someone who can offer meaningful value in the world, as long as they stay connected to their own natural rhythm rather than getting lost in other people's expectations.</p>
 </div>
