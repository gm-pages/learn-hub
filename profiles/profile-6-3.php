 <!-- ==================== WHAT IS THE 6/3 PROFILE? ==================== -->
 <section class="py-10 md:py-14 bg-gm-light">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> What Is the 6/3 Profile?
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 6/3 Profile combines the <strong class="text-gray-800">Role Model</strong>, Line 6, with the <strong class="text-gray-800">Martyr</strong>, Line 3. This creates a life process shaped by experimentation, adaptation, and eventual perspective. The 6th line moves through its three phases of development, while the unconscious 3rd line keeps life deeply experiential, often bringing lessons through direct contact with what works and what does not.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">Phase 1, around birth to 30: The 6/3 often lives like a 3/3, with a strong emphasis on trial and error, discovery, and learning through experience. This phase can feel especially intense because both sides of the profile are connected to experimentation and adaptation. Life may bring frequent changes, unexpected lessons, and a need to keep adjusting as understanding develops.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">Phase 2, around 30 to 50: The 6th line goes on the roof, bringing more observation, distance, and reflection. At the same time, the 3rd line continues to learn through direct experience, so this phase may still include experimentation even as a broader perspective begins to form. The result is often a mix of lived involvement and growing wisdom.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">Phase 3, around 50 and beyond: The 6th line comes off the roof and steps more fully into role model energy. By this stage, the 6/3 may carry a grounded kind of wisdom that comes from having tested life directly and learned through real experience. Its influence often feels credible not because life was perfect, but because what it knows has been genuinely lived.</p>
 </div>
 </section>
 <!-- ==================== HOW THE 6 AND 3 WORK TOGETHER ==================== -->
 <section class="py-10 md:py-14 bg-white">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6">
How the 6 and 3 Work Together
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 6/3 Profile brings together a role-model trajectory with deep learning through experience. Line 6 develops over time, gradually moving toward wisdom, perspective, and example. Line 3 learns by trying, adjusting, discovering, and responding to what does not work. Together, they create a profile shaped by growth through lived process.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">There can be tension here, especially earlier in life. The 6 carries the potential for perspective and insight, but the 3 often reaches that understanding through direct experimentation, correction, and adaptation. This can make the path feel messy before it feels coherent. Yet much of the eventual strength of the 6/3 comes from exactly that lived depth.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">When these two lines work well together, the result is <strong class="text-gray-800">wisdom with credibility</strong>. The 3 brings resilience, realism, and a willingness to learn through direct contact with life. The 6 brings perspective, integration, and the capacity to become an example for others. Together, they create someone whose role-model quality is not abstract, but deeply grounded in experience.</p>
 </div>
 </section>
 <!-- ==================== LIVING THE 6/3 ==================== -->
 <section class="py-10 md:py-14 bg-gm-light">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> Living the 6/3
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 6/3 path often asks for patience with process. The 3rd line learns through direct experience, adjustment, and discovery, while the 6th line matures over time into broader perspective and role-model presence. This can create a life that feels intense at times, especially early on, but it also gives the 6/3 the potential for deeply grounded wisdom.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">What supports the 6/3 most is not trying to avoid all mistakes or force early certainty. It is learning how to work with experience consciously. Strategy and Authority help the 6/3 move through experimentation in a way that is more aligned, less reactive, and more trustworthy over time. When this profile honors its timing, what it offers others comes from <strong class="text-gray-800">real resilience, honesty, and perspective</strong> rather than from pressure to have it all figured out too soon.</p>

 <div class="bg-gm-card-active border-l-4 border-gm-purple rounded-r-lg p-5 my-6">
 <p class="text-xs font-semibold text-gm-purple uppercase tracking-widest mb-2">Key Insight</p>
 <p class="text-gm-text text-sm leading-relaxed italic">Remember: your Profile operates within your Type and Authority. A 6/3 Generator lives this profile very differently from a 6/3 Projector. The Profile describes your role, while your Type and Authority describe how you enter into that role correctly.</p>
 </div>
 </div>
 </section>
 <!-- ==================== THE 6/3 IN RELATIONSHIPS ==================== -->
 <section class="py-10 md:py-14 bg-white">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> The 6/3 in Relationships
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">In relationships, the 6/3 often learns through direct experience. Early in life, this can bring a stronger pattern of experimentation, discovery, and adjustment as the profile figures out what is sustainable and what is not. Relationships may feel intense during this phase, not because the 6/3 is incapable of commitment, but because so much learning happens through lived experience.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">Over time, the 6th line brings more perspective, reflection, and discernment. Even so, the 3rd line continues to learn by testing life directly, which means the 6/3 often needs honesty, flexibility, and space for real growth within relationship. They tend to do best with people who do not expect perfection, but who value resilience, truthfulness, and the willingness to keep learning.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">In maturity, the 6/3 can bring unusual depth and realism to partnership. Their wisdom in relationship is often hard-won, but it can become deeply stabilizing when joined with self-awareness and correct timing. What they offer is not idealized romance, but something more grounded: <strong class="text-gray-800">honesty, experience, and the potential for deeply lived commitment</strong>.</p>
 </div>
