 <!-- ==================== THE 2/4 IN RELATIONSHIPS ==================== -->
 <section class="py-10 md:py-14 bg-white">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> The 2/4 in Relationships
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">In relationships, 2/4s need both privacy and connection. The 2nd line needs space, time alone, and the freedom to be itself without constant pressure or intrusion. The 4th line, meanwhile, is deeply relational and tends to form bonds through friendship, familiarity, and trusted social networks rather than through strangers.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">Because of this, 2/4 relationships often begin through people they already know or through a natural sense of connection within their community. This profile usually does not thrive on forced social effort or random pursuit. It wants relationships that feel warm, personal, and organically grown.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">A 2/4 also benefits from being with someone who can recognize their gifts, even when they do not fully see them themselves. The right partner does not drag them out of their shell, but helps draw them out naturally. When a 2/4 feels both respected in their solitude and welcomed in connection, they can be <strong class="text-gray-800">deeply loyal, natural, and quietly influential</strong> in love.</p>
