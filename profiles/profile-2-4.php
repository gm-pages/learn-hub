 <!-- ==================== WHAT IS THE 2/4 PROFILE? ==================== -->
 <section class="py-10 md:py-14 bg-gm-light">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> What Is the 2/4 Profile?
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 2/4 Profile combines the <strong class="text-gray-800">Hermit</strong>, Line 2, with the <strong class="text-gray-800">Opportunist</strong>, Line 4, creating one of the most paradoxical profiles in Human Design. The conscious 2nd line craves solitude and carries natural talents that often operate below conscious awareness. The unconscious 4th line needs community and finds opportunity through relationships. The 2/4 lives between the cave and the crowd.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 2nd line carries natural gifts, abilities that come so easily they may not even recognize them as special. The Hermit needs time alone to develop and integrate these gifts. They can feel uncomfortable when called upon, yet their talents often shine most clearly when others recognize and invite them to share.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 4th line ensures that the Hermit does not disappear entirely. Opportunities, relationships, and life direction tend to come through the network. A 2/4 needs people, but also needs to retreat from them regularly to recharge and return to their natural state.</p>
 </div>
 </section>
 <!-- ==================== HOW THE 2 AND 4 WORK TOGETHER ==================== -->
 <section class="py-10 md:py-14 bg-white">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6">
How the 2 and 4 Work Together
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 2/4 Profile brings together privacy and connection. Line 2 has a natural, self-contained quality. It often needs space, time alone, and freedom from interference in order to let its gifts emerge naturally. Line 4, by contrast, is social and relational. It opens through friendship, trusted networks, and the people who recognize and invite it into life.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">This creates a rhythm in which the 2/4 may move between retreat and engagement. Part of you needs to step back, protect your space, and develop in your own way. Another part of you is designed to be reached through relationships, opportunities, and personal connection. Life may work best when you are not forcing visibility, but also not shutting yourself off from the people who naturally open doors.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">When these two lines work well together, the result is a person whose <strong class="text-gray-800">natural gifts find the right audience</strong>. The 2 brings ease, instinct, and authenticity. The 4 brings warmth, connection, and the social bridges that help those gifts land in the world. Together, they create a profile that thrives when solitude and relationship are both honored.</p>
 </div>
 </section>
 <!-- ==================== LIVING THE 2/4 ==================== -->
 <section class="py-10 md:py-14 bg-gm-light">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> Living the 2/4
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 2/4 dance between solitude and social connection is not a problem to solve. It is the rhythm of this profile. The key is honoring both needs without letting one dominate. Too much isolation and the 4th line's opportunities can dry up. Too much socializing and the 2nd line's gifts do not have the space to develop.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 2/4 often needs others to mirror their talents back to them. They may not immediately see what is special about themselves until someone calls it out. The right recognition, from the right person, can unlock abilities the 2/4 did not realize they had.</p>

 <div class="bg-gm-card-active border-l-4 border-gm-purple rounded-r-lg p-5 my-6">
 <p class="text-xs font-semibold text-gm-purple uppercase tracking-widest mb-2">Key Insight</p>
 <p class="text-gm-text text-sm leading-relaxed italic">Remember: your Profile operates within your Type and Authority. A 2/4 Generator lives this profile very differently from a 2/4 Projector. The Profile describes your role, while your Type and Authority describe how you enter into that role correctly.</p>
 </div>
 </div>
