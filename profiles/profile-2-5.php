 <!-- ==================== WHAT IS THE 2/5 PROFILE? ==================== -->
 <section class="py-10 md:py-14 bg-gm-light">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> What Is the 2/5 Profile?
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 2/5 Profile combines the <strong class="text-gray-800">Hermit</strong>, Line 2, with the <strong class="text-gray-800">Heretic</strong>, Line 5, creating someone who naturally withdraws into their own world yet carries a powerful projection field that attracts others seeking solutions. The 2/5 is the reluctant savior, possessing natural gifts they may not fully recognize and being called upon by others who project onto them the ability to solve problems.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The conscious 2nd line needs solitude, carries innate talents, and prefers to be left alone. The unconscious 5th line operates through projection. Others see in the 2/5 what they need to see, often attributing qualities and abilities that may or may not be there. This projection field is powerful and unavoidable.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">When the 2/5 delivers on the projection, when their natural gifts actually solve the problem, their reputation can rise quickly. When they fail to meet the projection, the fall can be just as dramatic. This is part of the 5th line's universalizing quality: they are often cast as either the hero or the heretic, with very little middle ground.</p>
 </div>
 </section>
 <!-- ==================== HOW THE 2 AND 5 WORK TOGETHER ==================== -->
 <section class="py-10 md:py-14 bg-white">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6">
How the 2 and 5 Work Together
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 2/5 Profile combines a need for privacy with a powerful pull toward public relevance. Line 2 is naturally inward. It often prefers to be left alone, allowing its gifts to develop quietly and without pressure. Line 5, by contrast, lives in the field of other people's expectations. It tends to attract projections, responsibility, and the sense that others see it as someone who can solve problems.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">This can create a deep tension in the 2/5. Part of you wants space, simplicity, and the freedom to stay in your own rhythm. Another part of you may find that life keeps calling you into visibility, leadership, or practical usefulness. At times, this can feel intrusive, especially if others project onto you without really knowing you.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">When these two lines work well together, the result is a person who can <strong class="text-gray-800">offer practical value without losing their naturalness</strong>. The 2 brings authenticity and effortless gifts. The 5 brings impact and broad usefulness. Together, they create someone whose presence can be deeply helpful, provided they learn to engage selectively and not carry projections that do not belong to them.</p>
 </div>
 </section>
 <!-- ==================== LIVING THE 2/5 ==================== -->
 <section class="py-10 md:py-14 bg-gm-light">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> Living the 2/5
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 2/5 must learn to manage the projection field. Not every call is the right call. The 2nd line needs to be selective about when to emerge from hermit mode, and the 5th line needs to be honest about whether what is being projected onto them is something they can actually deliver.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">This is why Strategy and Authority matter so much for the 2/5. They help distinguish correct callings from projections that will end in disappointment. When a 2/5 responds to the right invitation, opportunity, or recognition, their natural gifts can have a deeply practical impact. When they try to live up to projections that are not truly theirs, the result is often frustration, pressure, and misunderstanding.</p>

 <div class="bg-gm-card-active border-l-4 border-gm-purple rounded-r-lg p-5 my-6">
 <p class="text-xs font-semibold text-gm-purple uppercase tracking-widest mb-2">Key Insight</p>
 <p class="text-gm-text text-sm leading-relaxed italic">Remember: your Profile operates within your Type and Authority. A 2/5 Generator lives this profile very differently from a 2/5 Projector. The Profile describes your role, while your Type and Authority describe how you enter into that role correctly.</p>
 </div>
 </div>
 </section>
 <!-- ==================== THE 2/5 IN RELATIONSHIPS ==================== -->
 <section class="py-10 md:py-14 bg-white">
 <div class="max-w-4xl mx-auto px-6">
 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-6"> The 2/5 in Relationships
 </h2>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">In relationships, 2/5s face a double challenge. The Hermit needs space, solitude, and the freedom to withdraw, while the 5th line attracts projections that partners may place on them. This can create confusion if the other person falls in love with an idea of the 2/5 rather than the real person.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">Because of this, 2/5s need relationships where they are seen clearly and not burdened with exaggerated expectations. They need room to retreat without being punished for it, and they need honesty about what they can and cannot provide. The more projection enters the relationship, the more important clarity becomes.</p>
 <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">When a 2/5 is with someone who respects their boundaries and recognizes their real gifts, they can be <strong class="text-gray-800">deeply supportive, practical, and stabilizing</strong>. They do best with partners who can separate fantasy from reality and love the actual person beneath the projection.</p>
 </div>
 </section>
