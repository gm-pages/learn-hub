 <h2 class="text-xl md:text-2xl font-bold text-gray-800 mb-8"> Frequently Asked Questions
 </h2>
 <div class="space-y-3">
 <details class="bg-white rounded-lg border border-gm-border">
 <summary class="py-4 px-6 text-sm font-semibold text-gray-800">What is a 2/5 Profile?</summary>
 <div class="px-6 pb-4 text-sm text-gray-600 leading-relaxed">The 2/5 combines the Hermit, Line 2, with the Heretic, Line 5. This profile carries natural gifts, needs solitude, and lives with a powerful projection field. Others often see the 2/5 as a problem-solver, and their reputation can rise or fall depending on whether they deliver on that projection.</div>
 </details>
 <details class="bg-white rounded-lg border border-gm-border">
 <summary class="py-4 px-6 text-sm font-semibold text-gray-800">What is the 5th line projection field?</summary>
 <div class="px-6 pb-4 text-sm text-gray-600 leading-relaxed">The 5th line carries a universalizing quality, which means other people often project their needs and expectations onto the person. This can create a hero or heretic dynamic. If the projection is met, they may be seen as a solution. If it is not, they may be blamed or rejected. Learning to manage this projection is a major part of the 2/5 life.</div>
 </details>
 <details class="bg-white rounded-lg border border-gm-border">
 <summary class="py-4 px-6 text-sm font-semibold text-gray-800">How do 2/5s know which callings to answer?</summary>
 <div class="px-6 pb-4 text-sm text-gray-600 leading-relaxed">Through Strategy and Authority. Not every call is correct. A 2/5 needs an inner way to tell the difference between a real opportunity and a projection that will lead to disappointment. Strategy and Authority help them answer the right calls and decline the wrong ones.</div>
 </details>
 <details class="bg-white rounded-lg border border-gm-border">
