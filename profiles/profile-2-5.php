                <span class="text-gm-purple">|</span> &#128300; What Is the 2/5 Profile?
            </h2>
            <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The 2/5 Profile combines the <strong class="text-gray-800">Hermit</strong> (Line 2) with the <strong class="text-gray-800">Heretic</strong> (Line 5) - creating someone who naturally withdraws into their own world yet carries a powerful projected field that attracts others seeking solutions. The 2/5 is the reluctant savior: possessing natural gifts they may not fully recognize, called upon by others who project onto them the ability to solve problems.</p>
            <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">The conscious 2nd line needs solitude, carries innate talents, and prefers to be left alone. The unconscious 5th line operates through <strong class="text-gray-800">projection</strong> - others see in the 2/5 what they need to see, often attributing qualities and abilities that may or may not be there. This projection field is powerful and unavoidable.</p>
            <p class="text-gm-text text-sm md:text-base leading-relaxed mb-4">When the 2/5 delivers on the projection - when their natural gifts actually solve the problem - their reputation soars. When they fail to meet the projection, the fall can be dramatic. This is the 5th line's universalizing quality: they are either the hero or the heretic, with little middle ground.</p>
        </div>
    </section>
